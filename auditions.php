	<h1 class="topic-header push-down">Frequently Asked Questions</h1>
	<p>If you have a question that is not answered here, send an email to <a href="mailto:adt-exec@mit.edu">adt-exec@mit.edu</a>.</p>
	<p>
		<span class="question">Q: How many people do you accept? What if I have no dance experience?</span><br />
		Anyone* who auditions for ADT will automatically be accepted regardless of experience, race, age, or gender. We believe in giving equal opportunity to experience the amazing culture of East Asian dance. Auditions are for the reference of the choreographers so that we may place you into the appropriate dances that fit your skill level, and you'll have the opportunity to select dances you're interested in.</p><p>*Due to liability issues, however, we are only able to accept undergraduate and graduate students.
	</p>
	<p>
		<span class="question">Q: What should I expect at auditions?</span><br />
		During auditions, the choreographers will teach two dances, one traditional and one hip-hop, from the semester's setlist and teach a sequence from each of them. Afterwards, auditionees will be asked to perform the sequence in groups. After you audition, the choreographers will show you the rest of the pieces we're learning this semester and you'll get to choose which ones you prefer and want to do.
	</p>
	<p>
		<span class="question">Q: What should I bring to audition?</span><br />
		Please wear comfortable clothing that is not too baggy, in addition to either ballet shoes or socks. Going barefoot is fine but not advised. Other than that, you should not need to bring anything else.
	</p>
	<p>
		<span class="question">Q: Will everyone get the dances they want?</span><br />
		Unfortunately, due to the difficulty of some of the dances and rehearsal scheduling, not everyone will be able to get the dance(s) they put down on their preference form at the audition session. However, the exec and choreographers are willing to devote as much time as possible to making sure everyone gets into at least one of the dances they put down on their form.
	</p>
	<p>
		<span class="question">Q: What if I can't make auditions?</span><br />
		Send an email to <a href="mailto:adt-exec@mit.edu">adt-exec@mit.edu</a> <b>before the date of auditions</b> and describe the reason for your absence. The exec board will try to schedule a make-up if possible, but <i>this is not guaranteed</i>. Please try to make the auditions date if at all possible. Thanks!
	</p>
